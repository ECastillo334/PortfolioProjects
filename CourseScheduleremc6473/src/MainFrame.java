    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String chosenClass = (String)scheduleClassComboBox.getSelectedItem();
        //String chosenStudent = (String)scheduleSelectStudentComboBox.getSelectedItem();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(scheduleSelectStudentComboBox.getSelectedIndex()).getStudentID();
        int courseSeats = ClassQueries.getClassSeats(currentSemesterLabel.getText(), chosenClass);
        if (ScheduleQueries.getScheduledStudentCount(currentSemesterLabel.getText(), chosenClass) < courseSeats){
            //DEBUG:
            //System.out.println(ScheduleQueries.getScheduledStudentCount(currentSemesterLabel.getText(), chosenClass) + " AND " + courseSeats);
            scheduleClassSubmitLabel.setText(chosenClass + " added to " + students.get(scheduleSelectStudentComboBox.getSelectedIndex()).getFirstName() + " " + students.get(scheduleSelectStudentComboBox.getSelectedIndex()).getLastName());
            Timestamp time = new Timestamp(System.currentTimeMillis());
            ScheduleQueries.addScheduleEntry(new ScheduleEntry("S", time, currentSemesterLabel.getText(), chosenClass, studentID));
        }
        else{
            scheduleClassSubmitLabel.setText(chosenClass + " class waitlisted for " + students.get(scheduleSelectStudentComboBox.getSelectedIndex()).getFirstName() + " " + students.get(scheduleSelectStudentComboBox.getSelectedIndex()).getLastName());
            Timestamp time = new Timestamp(System.currentTimeMillis());
            ScheduleQueries.addScheduleEntry(new ScheduleEntry("W", time, currentSemesterLabel.getText(), chosenClass, studentID));
        }
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

