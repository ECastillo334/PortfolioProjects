    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes(){
        ClassCodeJComboBox.removeAllItems();
        ArrayList<String> courses = CourseQueries.getAllCourseCodes();
        ClassCodeJComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
    }
    
    public void rebuildStudentComboBoxes(){
        scheduleSelectStudentComboBox.removeAllItems();
        selectStudentComboBox.removeAllItems();
        AdminDropClassStudentSelectComboBox.removeAllItems();
        DropStudentComboBox.removeAllItems();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> allNames = new ArrayList<String>();
        for (StudentEntry name : students){
            allNames.add(name.getFirstName() + " " + name.getLastName());
        }
        scheduleSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(allNames.toArray()));
        selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(allNames.toArray()));
        AdminDropClassStudentSelectComboBox.setModel(new javax.swing.DefaultComboBoxModel(allNames.toArray()));
        DropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(allNames.toArray()));
    }
    
