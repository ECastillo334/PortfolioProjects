    private void StudentDropClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StudentDropClassButtonActionPerformed
        // TODO add your handling code here:
        
        StudentDropClassTextArea.setText("");
        String currentSemester = currentSemesterLabel.getText();
        String currentClass = (String)studentDropClassChooseClassComboBox.getSelectedItem();
        
        int IDIndex = AdminDropClassStudentSelectComboBox.getSelectedIndex();
        ArrayList<StudentEntry> StudentList = StudentQueries.getAllStudents();
        StudentEntry theStudent = StudentList.get(IDIndex);
        ArrayList<ScheduleEntry> currentSchedule = ScheduleQueries.getScheduleByStudent(currentSemester, theStudent.getStudentID());
        String currentClassStatus = "W";
        for(ScheduleEntry getStatus: currentSchedule){
            if(getStatus.getCourseCode().equals(currentClass)){
                currentClassStatus = getStatus.getStatus();
            }
        }
        //DEBUG:
        //System.out.println(theStudent.getFirstName());
        ArrayList<ScheduleEntry> listHolder = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, currentClass);
        StudentDropClassTextArea.append(theStudent.getLastName() + ", " + theStudent.getFirstName() + " " + theStudent.getStudentID() + " has dropped " + currentClass + "\n");
        if(!listHolder.isEmpty() && currentClassStatus.equals("S")){
            ScheduleEntry waitlistedEntry = listHolder.getFirst();
            //DEBUG:
            //System.out.println(waitlistedEntry.getCourseCode());
            ScheduleQueries.updateScheduleEntry(waitlistedEntry);
            StudentEntry student = StudentQueries.getStudent(waitlistedEntry.getStudentID());
            StudentDropClassTextArea.append(student.getFirstName() + " " + student.getLastName() + " scheduled to class " + currentClass + "\n");
        }
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, theStudent.getStudentID(), currentClass);
    }//GEN-LAST:event_StudentDropClassButtonActionPerformed

    private void DropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentButtonActionPerformed
        // TODO add your handling code here:
        DropStudentTextArea.setText("");
        
        int IDIndex = DropStudentComboBox.getSelectedIndex();
        ArrayList<StudentEntry> StudentList = StudentQueries.getAllStudents();
        StudentEntry theStudent = StudentList.get(IDIndex);
        
        ArrayList<String> allSemester = SemesterQueries.getSemesterList();
        DropStudentTextArea.append(theStudent.getLastName() + ", " + theStudent.getFirstName() + " " + theStudent.getStudentID() + " removed\n" );
        
        for(String oneSemester:allSemester){
            
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(oneSemester, theStudent.getStudentID());
            DropStudentTextArea.append("For semester: " + oneSemester + "\n");
            for(ScheduleEntry currentClass : schedule){
                
                ArrayList<ScheduleEntry> listofWaitlisters = ScheduleQueries.getWaitlistedStudentsByClass(currentClass.getSemester(), currentClass.getCourseCode());
                
                if(currentClass.getStatus().equals("W")){
                    DropStudentTextArea.append(theStudent.getLastName() + ", " + theStudent.getFirstName() + " " + theStudent.getStudentID() + " removed from waitlist for " + currentClass.getCourseCode() + "\n");
                }
                else{
                    DropStudentTextArea.append(theStudent.getLastName() + ", " + theStudent.getFirstName() + " " + theStudent.getStudentID() + " removed from class " + currentClass.getCourseCode() + "\n");
                }

                if(!listofWaitlisters.isEmpty() && currentClass.getStatus().equals("S")){
                    ScheduleEntry waitlistedEntry = listofWaitlisters.getFirst();
                    ScheduleQueries.updateScheduleEntry(waitlistedEntry); 
                    StudentEntry student = StudentQueries.getStudent(waitlistedEntry.getStudentID());
                    DropStudentTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + " scheduled to class " + currentClass.getCourseCode() + " for " + currentClass.getSemester() + "\n");
                }
                
                ScheduleQueries.dropStudentScheduleByCourse(currentClass.getSemester(), theStudent.getStudentID(), currentClass.getCourseCode());
            }
        
        }
        
        StudentQueries.dropStudent(theStudent.getStudentID());
        rebuildStudentComboBoxes();
        
        rebuildSemesterComboBoxes();
        
        
    }//GEN-LAST:event_DropStudentButtonActionPerformed

    private void AdminDropClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdminDropClassButtonActionPerformed
        // TODO add your handling code here:
        AdminDropClassTextArea.setText("");
        String semester = currentSemesterLabel.getText();
        String currentClass = (String)AdminDropClassComboBox.getSelectedItem();
        
        ArrayList<StudentEntry> allCurrentStudents = MultiTableQueries.getScheduledStudentsByClass(semester, currentClass);
        ArrayList<StudentEntry> allWaitlistedStudents = MultiTableQueries.getWaitlistedStudentsByClass(semester, currentClass);
        
        ClassQueries.dropClass(semester, currentClass);
        ScheduleQueries.dropScheduleByCourse(semester, currentClass);
        
        AdminDropClassTextArea.append(currentClass + " course removed for " + semester + " semester" + "\n");
        for(StudentEntry student: allCurrentStudents){
           AdminDropClassTextArea.append(student.getFirstName() + " " + student.getLastName() + " " + student.getStudentID() + " removed from class\n"); 
        }
        
        for(StudentEntry student: allWaitlistedStudents){
           AdminDropClassTextArea.append(student.getFirstName() + " " + student.getLastName() + " " + student.getStudentID() + " removed from waitlist\n"); 
        }
        
        rebuildClassComboBoxes();
    }//GEN-LAST:event_AdminDropClassButtonActionPerformed

